package datamining.cn;

public class block {
	String vl;
	int n;
	public block()
	{
		
	}
	public block(String vl)
	{
		this.vl=vl;
		this.n=0;
	}
	public String getVl() {
		return vl;
	}
	public void setVl(String vl) {
		this.vl = vl;
	}
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}

}
